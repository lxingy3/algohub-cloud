import assert from 'node:assert/strict';
import { mkdir, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { createBriefingPdf } from '../app/briefings/[slug]/briefingPdf.js';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const outputDir = path.join(root, 'output', 'pdf');
const repeated = 'Residents described delayed notices, confusing appeal instructions, and inconsistent explanations across service channels. Staff should publish a plain-language decision path and record whether each handoff reached a resolution.';
const sample = {
  slug: 'sample-week-11-briefing',
  title: 'Resident experiences with automated eligibility and service routing',
  type: 'Cross-cutting briefing',
  subject: 'Public benefits and resident services',
  executiveSummary: `${repeated} ${repeated}`,
  metadata: [
    { label: 'Approved stories', value: '73' },
    { label: 'Evidence window', value: 'Jan 2, 2026 - Jul 24, 2026' },
    { label: 'Review status', value: 'Published' },
    { label: 'Reviewed by', value: 'Community Editorial Team' },
    { label: 'Published', value: 'Jul 24, 2026' },
    { label: 'Generated by', value: 'Staff-generated evidence draft' },
  ],
  patternAnalysis: repeated,
  keyFindings: Array.from({ length: 8 }, (_, index) => `Finding ${index + 1}. ${repeated}`),
  recommendations: Array.from({ length: 6 }, (_, index) => `Action ${index + 1}. ${repeated}`),
  claimGroups: Array.from({ length: 3 }, (_, index) => ({
    name: `Service system ${index + 1}`,
    experienceCount: 12 + index,
    matchMethod: 'Curated story-system link',
    claims: [
      { text: 'The system reduces processing time and makes routing more consistent.', source: 'Agency documentation' },
      { text: repeated, source: 'Program description' },
    ],
    examples: [
      { title: 'A resident received two different deadline notices.', impact: 'Negative' },
      { title: 'A navigator helped a resident complete the appeal.', impact: 'Mixed' },
    ],
  })),
  silenceRows: Array.from({ length: 12 }, (_, index) => ({
    system: `Synthetic system ${index + 1}`,
    priority: index % 2 ? 'Monitor' : 'Outreach',
    volume: `${index + 1} / 12`,
    score: (0.25 + index / 100).toFixed(2),
  })),
  provenance: [
    { label: 'Review', value: 'Published after editorial review on Jul 24, 2026.' },
    { label: 'Partner review', value: 'Two assigned partner organizations approved this publication.' },
    { label: 'Publication', value: 'Published from a snapshot of 73 approved stories.' },
    { label: 'Generation', value: 'Narrative and computed groupings remain traceable to this briefing record.' },
    { label: 'Evidence boundary', value: repeated },
  ],
};

await mkdir(outputDir, { recursive: true });
const doc = createBriefingPdf(sample);
const pages = doc.getNumberOfPages();
assert.ok(pages > 1, 'stress sample should paginate');
assert.ok(pages <= 8, `stress sample produced too many pages: ${pages}`);
const bytes = Buffer.from(doc.output('arraybuffer'));
assert.equal(bytes.subarray(0, 5).toString('ascii'), '%PDF-');
assert.ok(bytes.length > 6_000, 'PDF is unexpectedly small');
const outputPath = path.join(outputDir, 'briefing-sample.pdf');
await writeFile(outputPath, bytes);
console.log(`portrait: ${pages} pages, ${bytes.length} bytes -> ${outputPath}`);
